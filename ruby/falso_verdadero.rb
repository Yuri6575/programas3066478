# variables con los valores booleanos true y false
a = true
b = false
puts a && b  # Operación 'and' en Ruby, equivalente a '&&'

# Definimos variables numéricas
a = 2
b = 3
c = 4
d = 10

# Comparación usando 'and' (&&)
puts (a == b && c < d)

# Comparación usando 'not' (en Ruby se usa '!' para negar)
puts !(a == b && c > d)
