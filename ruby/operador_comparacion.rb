a = 10
b = 5

puts "\n Operadores de comparación:"
puts "¿a es igual a b? #{a == b}"  # Igual a
puts "¿a es diferente de b? #{a != b}"  # Diferente de
puts "¿a es mayor que b? #{a > b}"    # Mayor que
puts "¿a es menor que b? #{a < b}"    # Menor que
puts "¿a es mayor o igual a b? #{a >= b}"  # Mayor o igual que
puts "¿a es menor o igual a b? #{a <= b}"  # Menor o igual que